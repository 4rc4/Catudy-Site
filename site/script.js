const header = document.querySelector(".site-header");
const revealItems = document.querySelectorAll(".reveal");
const scene = document.querySelector(".hero-scene");
const languageSwitcher = document.querySelector(".language-switcher");
const languageButtons = document.querySelectorAll("[data-lang]");
const textTargets = document.querySelectorAll("[data-i18n]");
const ariaTargets = document.querySelectorAll("[data-i18n-aria]");
const altTargets = document.querySelectorAll("[data-i18n-alt]");
const contentTargets = document.querySelectorAll("[data-i18n-content]");
let currentLanguage = "tr";
let languageAnimationTimer = 0;

const getSavedLanguage = () => {
  try {
    return window.localStorage.getItem("catudy-language");
  } catch {
    return null;
  }
};

const saveLanguage = (language) => {
  try {
    window.localStorage.setItem("catudy-language", language);
  } catch {
    // Language switching still works when storage is unavailable.
  }
};

const setLanguage = (language) => {
  const activeLanguage = translations[language] ? language : "tr";
  const strings = translations[activeLanguage];

  document.documentElement.lang = activeLanguage;
  document.title = strings.documentTitle;

  textTargets.forEach((target) => {
    const key = target.dataset.i18n;
    if (strings[key]) {
      target.textContent = strings[key];
    }
  });

  ariaTargets.forEach((target) => {
    const key = target.dataset.i18nAria;
    if (strings[key]) {
      target.setAttribute("aria-label", strings[key]);
    }
  });

  altTargets.forEach((target) => {
    const key = target.dataset.i18nAlt;
    if (strings[key]) {
      target.setAttribute("alt", strings[key]);
    }
  });

  contentTargets.forEach((target) => {
    const key = target.dataset.i18nContent;
    if (strings[key]) {
      target.setAttribute("content", strings[key]);
    }
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === activeLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  currentLanguage = activeLanguage;
  saveLanguage(activeLanguage);
};

const animateLanguageChange = (language) => {
  const nextLanguage = translations[language] ? language : "tr";

  if (nextLanguage === currentLanguage) {
    return;
  }

  window.clearTimeout(languageAnimationTimer);
  document.body.classList.remove("is-language-entering");
  document.body.classList.add("is-language-exiting");
  languageSwitcher?.classList.add("is-switching");
  languageButtons.forEach((button) => {
    button.disabled = true;
  });

  languageAnimationTimer = window.setTimeout(() => {
    setLanguage(nextLanguage);
    document.body.classList.remove("is-language-exiting");
    document.body.classList.add("is-language-entering");

    languageAnimationTimer = window.setTimeout(() => {
      document.body.classList.remove("is-language-entering");
      languageSwitcher?.classList.remove("is-switching");
      languageButtons.forEach((button) => {
        button.disabled = false;
      });
    }, 340);
  }, 170);
};

languageButtons.forEach((button) => {
  button.addEventListener("click", () => animateLanguageChange(button.dataset.lang));
});

setLanguage(getSavedLanguage() || "tr");

let lastScrollY = window.scrollY;

const updateHeader = () => {
  const currentScrollY = window.scrollY;
  const scrollDelta = currentScrollY - lastScrollY;

  header.classList.toggle("is-scrolled", currentScrollY > 18);

  if (currentScrollY < 90 || scrollDelta < -6) {
    header.classList.remove("is-hidden");
  } else if (scrollDelta > 8) {
    header.classList.add("is-hidden");
  }

  lastScrollY = currentScrollY;
};

updateHeader();
window.addEventListener("scroll", updateHeader, { passive: true });

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.18 },
);

revealItems.forEach((item) => revealObserver.observe(item));

if (scene && !window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
  window.addEventListener(
    "pointermove",
    (event) => {
      const x = (event.clientX / window.innerWidth - 0.5) * 14;
      const y = (event.clientY / window.innerHeight - 0.5) * 14;
      scene.style.setProperty("--tilt-x", `${x}px`);
      scene.style.setProperty("--tilt-y", `${y}px`);
      scene.animate(
        {
          transform: `translate3d(${x}px, ${y}px, 0)`,
        },
        {
          duration: 900,
          fill: "forwards",
          easing: "cubic-bezier(.2,.8,.2,1)",
        },
      );
    },
    { passive: true },
  );
}
